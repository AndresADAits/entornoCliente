function resta(num1, num2) {
    return num1 - num2;
}
function multiplicacion(num1, num2) {
    return num1 * num2;
    /*Si solo meto un numero devuelve
    NaN, Not a Number
    */
}
function division(num1, num2) {
    return num1 / num2;
}
function asignacion(num1, num2) {
    return num1 += num2;
}
function relacion(num1, num2) {
    return num1 >= num2;
}
function igualdad(num1, num2) {
    return num1 == num2;
}
function igualdadEx(num1, num2) {
    return num1 === num2;
}
